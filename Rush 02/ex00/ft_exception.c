#include "ft_h.h"

int		ft_check_int(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (!(s[i] >= '0' && s[i] <= '9'))
			return (1);
		i++;
	}
	return (0);
}

int     ft_check_print_able(char *s)
{
	int i;

	i = 0;
	while (s[i] != '\n')
	{
		if (!(s[i] >= 32 && s[i] <= 126))
			return(1);
		i++;
	}
	return (0);
}
