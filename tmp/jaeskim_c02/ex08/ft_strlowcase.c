char	*ft_strlowcase(char *str)
{
	char *cp;

	cp = str;
	while (*cp != '\0')
	{
		if ('A' <= *cp && *cp <= 'Z')
			*cp = *cp + 32;
		cp++;
	}
	return (str);
}
