#include <unistd.h>

void	ft_putstr(char *str)
{
	char	tmp;
	int		i;

	i = 0;
	while (*(str + i) != 0)
	{
		tmp = *(str + i);
		write(1, &tmp, 1);
		i++;
	}
}
