int		ft_any(char **tab, int (*f)(char *))
{
	int i;
	int result;

	i = 0;
	while (tab[i] != 0)
	{
		result = f(tab[i]);
		if (result != 0)
			return (1);
		i++;
	}
	return (0);
}
