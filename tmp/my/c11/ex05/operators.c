int	add(int n1, int n2)
{
	return (n1 + n2);
}

int	sub(int n1, int n2)
{
	return (n1 - n2);
}

int	multi(int n1, int n2)
{
	return (n1 * n2);
}

int	divid(int n1, int n2)
{
	return (n1 / n2);
}

int	mod(int n1, int n2)
{
	return (n1 % n2);
}
