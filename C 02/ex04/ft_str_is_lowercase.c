int		ft_str_is_lowercase(char *str)
{
	int answer;

	answer = 1;
	while (*str != '\0')
	{
		if (!(*str >= 97 && *str <= 122))
		{
			answer = 0;
		}
		str++;
	}
	return (answer);
}
