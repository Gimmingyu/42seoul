int		ft_islowercase(char *str)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] < 97 || str[i] > 122)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
